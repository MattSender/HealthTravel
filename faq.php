              Pour installer le boîtier de mesure environnementale dans votre voiture, voici les étapes à suivre :   
              <br> 
              <br>         
              1. Trouvez un emplacement approprié pour le boîtier dans votre voiture, qui doit être accessible au conducteur. Les emplacements recommandés sont soit proche de la radio et de l’écran central, soit côté gauche du conducteur.
              <br>
              <br>
              2. Assurez-vous que le boîtier est équipé de toutes les pièces nécessaires, telles que les câbles et les capteurs.
              <br>
              <br>
              3. Branchez les câbles fournis dans les ports appropriés du boîtier à l’aide du manuel utilisateur.
              <br>
              <br>
              4. Allumez le boîtier et vérifiez que toutes les mesures environnementales sont correctement affichées.
              <br>
              <br>
              5. Si nécessaire, réglez les paramètres du boîtier en fonction de vos préférences.
